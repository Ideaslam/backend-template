using System.Collections.Generic;
using System.Drawing;
using System.Drawing.Drawing2D;
using System.Drawing.Imaging;
using System.IO;
using System.Text;

namespace Edura.Utils.Helpers
{
    public static class ImageHelper
    {
        public static Image LoadImage()
